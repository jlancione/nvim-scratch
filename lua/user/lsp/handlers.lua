M.setup = function() -- qsto pke' in realta' qsto file lo usiamo in 1 modo particolare ie nn ne facciamo solo 1 require(handlers) ma facciamo require("path.handlers").setup()! e in altri file ne richiamiamo altri metodi!
  local signs = {
    { name = "DiagnosticSignError", text = "" },
    { name = "DiagnosticSignWarn", text = "" },
    { name = "DiagnosticSignHint", text = "" },
    { name = "DiagnosticSignInfo", text = "" },
  }

  for _, sign in ipairs(signs) do
    vim.fn.sign_define(sign.name, { texthl = sign.name, text = sign.text, numhl = "" })
  end

  local config = {
    -- disable virtual text
    virtual_text = false,
    -- show signs
    signs = {
      active = signs,
    },
    update_in_insert = true,
    underline = true,
    severity_sort = true,
    float = {
      focusable = false,
      style = "minimal",
      border = "rounded",
      source = "always",
      header = "",
      prefix = "",
    },
  }

  vim.diagnostic.config(config)

  vim.lsp.handlers["textDocument/hover"] = vim.lsp.with(vim.lsp.handlers.hover, {
    border = "rounded",
  }) -- qsto e' clamoroso! (in realta' qua stai solo facendo in modo che i bordi siano rounded) ma con shift-k ti sputa il popup dla man page (o almeno qlche info) del comando su cui ti trovi -> in realta qsta e' 1 dei bindings che stai definendo sotto...

  vim.lsp.handlers["textDocument/signatureHelp"] = vim.lsp.with(vim.lsp.handlers.signature_help, {
    border = "rounded",
  })
